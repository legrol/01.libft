#include <stdio.h>
#include <stdlib.h>
#include "libft.h"

/**
 * The ft_lstadd_back() function add the 'new' node to the end of the 'lst'
 * list. 
 * 
 * @param t_list **lst  The pointer to the first node in a list.
 * @param t_list *new   The pointer to a node to add to the list.
 *  
 * return Void.
 * 
*/

void	ft_lstadd_back(t_list **lst, t_list *new)
{
	t_list	*tmp;

	if (!lst || !new)
		return ;
	if (!(*lst))
	{
		*lst = new;
		return ;
	}
	tmp = *lst;
	while (tmp->next)
		tmp = tmp->next;
	tmp->next = new;
}
