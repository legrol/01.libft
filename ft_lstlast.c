#include <stdio.h>
#include <stdlib.h>
#include "libft.h"

/**
 * The ft_lstlast() function returns the last node in the list. 
 * 
 * @param t_list *lst  The beginning of the list.
 *  
 * return The last node in the list.
 * 
*/

t_list	*ft_lstlast(t_list *lst)
{
	if (!lst)
		return (NULL);
	while (lst->next)
	{
		lst = lst->next;
	}
	return (lst);
}
